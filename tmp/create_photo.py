import os

base = "/Users/euijungner/aring-web"
photo_dir = os.path.join(base, "app", "search", "photo")
os.makedirs(photo_dir, exist_ok=True)

lines = []
w = lines.append

w("'use client';")
w("import { useEffect, useState } from 'react';")
w("import { useRouter } from 'next/navigation';")
w("import { createBrowserClient } from '@supabase/ssr';")
w("import Image from 'next/image';")
w("")
w("const ANON_ID_KEY = 'aring_anon_user_id';")
w("")
w("interface MyListing {")
w("  id: string;")
w("  title: string | null;")
w("  brand: string | null;")
w("  photo_url: string | null;")
w("  status: string;")
w("}")
w("")
w("function IconChevronRight() {")
w("  return (")
w("    <svg width='16' height='16' viewBox='0 0 24 24' fill='none' stroke='currentColor' strokeWidth='2'>")
w("      <polyline points='9 18 15 12 9 6'/>")
w("    </svg>")
w("  );")
w("}")
w("")
w("function statusLabel(status: string) {")
w("  if (status === 'open') return '찾는 중';")
w("  if (status === 'matched') return '매칭 완료';")
w("  if (status === 'closed') return '마감';")
w("  return status;")
w("}")
w("")
w("function statusColor(status: string) {")
w("  if (status === 'open') return 'bg-green-100 text-green-700';")
w("  if (status === 'matched') return 'bg-amber-100 text-amber-700';")
w("  return 'bg-gray-100 text-gray-500';")
w("}")
w("")
w("export default function SearchPhotoPage() {")
w("  const router = useRouter();")
w("  const [listings, setListings] = useState<MyListing[]>([]);")
w("  const [loading, setLoading] = useState(true);")
w("")
w("  useEffect(() => {")
w("    const supabase = createBrowserClient(")
w("      process.env.NEXT_PUBLIC_SUPABASE_URL!,")
w("      process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!")
w("    );")
w("    (async () => {")
w("      const { data: { session } } = await supabase.auth.getSession();")
w("      let userId = session?.user?.id ?? '';")
w("      if (!userId) {")
w("        userId = localStorage.getItem(ANON_ID_KEY) ?? '';")
w("      }")
w("      if (!userId) { setLoading(false); return; }")
w("")
w("      const { data } = await supabase")
w("        .from('listings')")
w("        .select('id, title, brand, photo_url, status')")
w("        .eq('user_id', userId)")
w("        .order('created_at', { ascending: false });")
w("      setListings(data ?? []);")
w("      setLoading(false);")
w("    })();")
w("  }, []);")
w("")
w("  return (")
w("    <div className='min-h-screen bg-white'>")
w("      <header className='sticky top-0 z-10 bg-white border-b border-gray-100'>")
w("        <div className='flex items-center gap-3 px-4 h-14 max-w-lg mx-auto'>")
w("          <button onClick={() => router.back()} className='p-1 -ml-1 text-gray-600 hover:text-gray-900' aria-label='뒤로'>")
w("            <svg width='24' height='24' viewBox='0 0 24 24' fill='none' stroke='currentColor' strokeWidth='2'>")
w("              <polyline points='15 18 9 12 15 6'/>")
w("            </svg>")
w("          </button>")
w("          <h1 className='text-base font-semibold text-gray-900'>내 귀걸이 매칭 현황</h1>")
w("        </div>")
w("      </header>")
w("")
w("      <div className='max-w-lg mx-auto px-4 py-6'>")
w("        {loading && (")
w("          <div className='flex justify-center py-20'>")
w("            <div className='w-6 h-6 border-2 border-gray-300 border-t-gray-700 rounded-full animate-spin' />")
w("          </div>")
w("        )}")
w("")
w("        {!loading && listings.length === 0 && (")
w("          <div className='flex flex-col items-center justify-center py-20 text-center'>")
w("            <p className='text-base font-semibold text-gray-700 mb-1'>등록한 귀걸이가 없어요</p>")
w("            <p className='text-sm text-gray-400 mb-6'>한 짝 귀걸이를 등록하면 짝을 찾아드려요.</p>")
w("            <button onClick={() => router.push('/register')} className='px-6 py-3 bg-gray-900 text-white rounded-full text-sm font-semibold hover:bg-gray-700 transition-colors'>")
w("              한 짝 등록하기")
w("            </button>")
w("          </div>")
w("        )}")
w("")
w("        {!loading && listings.length > 0 && (")
w("          <div>")
w("            <p className='text-sm text-gray-400 mb-4'>귀걸이를 선택하면 최신 매칭 결과를 볼 수 있어요.</p>")
w("            <div className='flex flex-col gap-3'>")
w("              {listings.map((item) => (")
w("                <button")
w("                  key={item.id}")
w("                  onClick={() => router.push('/match/' + item.id)}")
w("                  className='flex items-center gap-4 p-4 border border-gray-100 rounded-2xl bg-white hover:bg-gray-50 hover:border-gray-200 transition-all text-left shadow-sm'")
w("                >")
w("                  <div className='relative w-16 h-16 rounded-xl overflow-hidden bg-gray-100 flex-shrink-0'>")
w("                    {item.photo_url ? (")
w("                      <Image src={item.photo_url} alt={item.title ?? '귀걸이'} fill className='object-cover' />")
w("                    ) : (")
w("                      <div className='w-full h-full flex items-center justify-center text-gray-300 text-xs'>사진 없음</div>")
w("                    )}")
w("                  </div>")
w("                  <div className='flex-1 min-w-0'>")
w("                    <p className='text-sm font-semibold text-gray-900 line-clamp-1'>{item.title ?? '귀걸이'}</p>")
w("                    {item.brand && <p className='text-xs text-gray-400 mt-0.5'>{item.brand}</p>}")
w("                    <span className={'inline-block mt-1.5 px-2 py-0.5 rounded-full text-xs font-medium ' + statusColor(item.status)}>")
w("                      {statusLabel(item.status)}")
w("                    </span>")
w("                  </div>")
w("                  <div className='text-gray-300 flex-shrink-0'>")
w("                    <IconChevronRight />")
w("                  </div>")
w("                </button>")
w("              ))}")
w("            </div>")
w("          </div>")
w("        )}")
w("      </div>")
w("    </div>")
w("  );")
w("}")

content = "\n".join(lines)
with open(os.path.join(photo_dir, "page.tsx"), "w", encoding="utf-8") as f:
    f.write(content)
print("Done!")