import os

base = "/Users/euijungner/aring-web"
os.makedirs(os.path.join(base, "app", "my", "match"), exist_ok=True)

lines = []
w = lines.append

w("'use client';")
w("import { useEffect, useState } from 'react';")
w("import { useRouter } from 'next/navigation';")
w("import { supabase } from '@/lib/supabase';")
w("import { calculateAringMatch } from '@/lib/aringMatch';")
w("import Image from 'next/image';")
w("")
w("const ANON_ID_KEY = 'aring_anon_user_id';")
w("")
w("interface MyListing {")
w("  id: string;")
w("  brand: string | null;")
w("  shape: string | null;")
w("  detail: string | null;")
w("  color: string | null;")
w("  material: string | null;")
w("  photo_url: string;")
w("  status: 'open' | 'matched' | 'closed';")
w("  view_count: number;")
w("}")
w("")
w("interface MatchSummary {")
w("  similar: number;")
w("  reference: number;")
w("  topScore: number;")
w("}")
w("")
w("function statusLabel(s: string) {")
w("  if (s === 'open') return '찾는 중';")
w("  if (s === 'matched') return '매칭 완료';")
w("  return '마감';")
w("}")
w("")
w("function statusColor(s: string) {")
w("  if (s === 'open') return 'bg-green-50 text-green-700 border-green-200';")
w("  if (s === 'matched') return 'bg-amber-50 text-amber-700 border-amber-200';")
w("  return 'bg-gray-50 text-gray-500 border-gray-200';")
w("}")
w("")
w("function scoreRange(top: number) {")
w("  if (top >= 80) return 'aring Match 80~100%';")
w("  if (top >= 60) return 'aring Match 60~79%';")
w("  if (top >= 40) return 'aring Match 40~59%';")
w("  return 'aring Match 분석중';")
w("}")
w("")
w("function scoreRangeColor(top: number) {")
w("  if (top >= 80) return 'bg-amber-50 text-amber-700 border-amber-200';")
w("  if (top >= 60) return 'bg-green-50 text-green-700 border-green-200';")
w("  if (top >= 40) return 'bg-blue-50 text-blue-600 border-blue-200';")
w("  return 'bg-gray-50 text-gray-400 border-gray-200';")
w("}")
w("")
w("function matchSummaryText(m: MatchSummary | null) {")
w("  if (!m) return '아직 비슷한 후보를 찾지 못했어요';")
w("  if (m.similar === 0 && m.reference === 0) return '아직 비슷한 후보를 찾지 못했어요';")
w("  return '유사한 후보 ' + m.similar + '개 · 참고 후보 ' + m.reference + '개를 찾았어요';")
w("}")
w("")
w("export default function MyMatchPage() {")
w("  const router = useRouter();")
w("  const [listings, setListings] = useState<MyListing[]>([]);")
w("  const [matchMap, setMatchMap] = useState<Record<string, MatchSummary>>({});")
w("  const [loading, setLoading] = useState(true);")
w("")
w("  useEffect(() => {")
w("    if (!supabase) { setLoading(false); return; }")
w("    supabase.auth.getSession().then(async ({ data: { session } }) => {")
w("      const userId = session?.user?.id ?? localStorage.getItem(ANON_ID_KEY) ?? '';")
w("      if (!userId) { setLoading(false); return; }")
w("      const { data: myItems } = await supabase!")
w("        .from('listings')")
w("        .select('id, brand, shape, detail, color, material, photo_url, status, view_count')")
w("        .eq('user_id', userId)")
w("        .order('created_at', { ascending: false });")
w("      const items = (myItems ?? []) as MyListing[];")
w("      if (items.length === 1) {")
w("        router.replace('/match/' + items[0].id);")
w("        return;")
w("      }")
w("      setListings(items);")
w("      if (items.length > 0) {")
w("        const { data: allOpen } = await supabase!")
w("          .from('listings')")
w("          .select('id, brand, shape, detail, color, material')")
w("          .eq('status', 'open')")
w("          .neq('user_id', userId)")
w("          .limit(200);")
w("        const candidates = allOpen ?? [];")
w("        const map: Record<string, MatchSummary> = {};")
w("        for (const item of items) {")
w("          const src = { shape: item.shape ?? '', color: item.color ?? '', material: item.material ?? '', detail: item.detail ?? '', brand: item.brand };")
w("          let sim = 0, ref = 0, top = 0;")
w("          for (const c of candidates) {")
w("            const tgt = { shape: c.shape ?? '', color: c.color ?? '', material: c.material ?? '', detail: c.detail ?? '', brand: c.brand };")
w("            const r = calculateAringMatch(src, tgt);")
w("            if (r.totalScore >= 40) {")
w("              if (r.totalScore >= 60) sim++; else ref++;")
w("              if (r.totalScore > top) top = r.totalScore;")
w("            }")
w("          }")
w("          map[item.id] = { similar: sim, reference: ref, topScore: top };")
w("        }")
w("        setMatchMap(map);")
w("      }")
w("      setLoading(false);")
w("    });")
w("  }, [router]);")
w("")
w("  if (loading) return (")
w("    <div className='min-h-screen bg-white flex items-center justify-center'>")
w("      <div className='w-6 h-6 border-2 border-gray-300 border-t-aring-ink-900 rounded-full animate-spin' />")
w("    </div>")
w("  );")
w("")
w("  if (listings.length === 0) return (")
w("    <div className='min-h-screen bg-white'>")
w("      <div className='px-5 lg:px-8 pt-5 lg:pt-8 pb-3'>")
w("        <h1 className='text-[22px] font-extrabold tracking-tight text-aring-ink-900'>내 귀걸이 매칭 현황</h1>")
w("        <p className='text-sm text-aring-ink-400 mt-1'>귀걸이 매칭을 기다리는 나의 귀걸이 0건</p>")
w("      </div>")
w("      <div className='mx-4 lg:mx-8 mt-4 rounded-2xl bg-white shadow-card border border-aring-ink-100 flex flex-col items-center justify-center py-16 px-6 text-center'>")
w("        <p className='text-base font-bold text-aring-ink-900 mb-1'>아직 매칭을 기다리는 귀걸이가 없어요</p>")
w("        <p className='text-sm text-aring-ink-400 mb-6 leading-relaxed'>한 짝 남은 귀걸이를 등록하면<br/>비슷한 후보를 찾아볼 수 있어요</p>")
w("        <button onClick={() => router.push('/register')} className='px-6 py-3 bg-aring-ink-900 text-white rounded-full text-sm font-semibold hover:bg-aring-ink-700 transition-colors'>")
w("          귀걸이 등록하러 가기")
w("        </button>")
w("      </div>")
w("    </div>")
w("  );")
w("")
w("  return (")
w("    <div className='min-h-screen bg-white'>")
w("      {/* 타이틀 - comments 페이지와 동일한 구조 */}")
w("      <div className='px-5 lg:px-8 pt-5 lg:pt-8 pb-3'>")
w("        <h1 className='text-[22px] font-extrabold tracking-tight text-aring-ink-900'>내 귀걸이 매칭 현황</h1>")
w("        <p className='text-sm text-aring-ink-400 mt-1'>귀걸이 매칭을 기다리는 나의 귀걸이 {listings.length}건</p>")
w("      </div>")
w("")
w("      {/* PC 테이블 - comments 페이지와 동일한 구조 */}")
w("      <div className='hidden lg:block mt-2 mx-6 rounded-2xl bg-white shadow-card border border-aring-ink-100 overflow-hidden'>")
w("        <div className='flex items-center gap-3 px-6 py-2.5 bg-aring-ink-50 border-b border-aring-ink-100 text-[11px] font-bold text-aring-ink-500 uppercase tracking-wide'>")
w("          <div className='w-10 text-center flex-shrink-0'>No.</div>")
w("          <div className='w-14 flex-shrink-0'>사진</div>")
w("          <div className='w-28 flex-shrink-0'>브랜드명</div>")
w("          <div className='w-16 flex-shrink-0'>상태</div>")
w("          <div className='flex-1 min-w-0'>매칭 결과 요약</div>")
w("          <div className='w-36 flex-shrink-0'>aring Match</div>")
w("          <div className='w-10 flex-shrink-0 text-right'>조회</div>")
w("        </div>")
w("        {listings.map((item, idx) => {")
w("          const m = matchMap[item.id] ?? null;")
w("          return (")
w("            <div key={item.id} onClick={() => router.push('/match/' + item.id)}")
w("              className='flex items-center gap-3 px-6 py-3 hover:bg-aring-ink-50 transition-colors border-b border-aring-ink-100 last:border-b-0 cursor-pointer'>")
w("              <div className='w-10 text-center flex-shrink-0 text-[11px] font-bold text-aring-ink-400'>{listings.length - idx}</div>")
w("              <div className='w-14 flex-shrink-0'>")
w("                <div className='relative w-12 h-12 rounded-xl overflow-hidden bg-aring-ink-100'>")
w("                  {item.photo_url && <Image src={item.photo_url} alt={item.brand ?? ''} fill className='object-cover' />}")
w("                </div>")
w("              </div>")
w("              <div className='w-28 flex-shrink-0 text-sm font-semibold text-aring-ink-900 truncate'>{item.brand ?? '브랜드 미상'}</div>")
w("              <div className='w-16 flex-shrink-0'>")
w("                <span className={'inline-flex items-center px-2 py-0.5 rounded-full text-[10px] font-semibold border ' + statusColor(item.status)}>")
w("                  {statusLabel(item.status)}")
w("                </span>")
w("              </div>")
w("              <div className='flex-1 min-w-0 text-sm text-aring-ink-600'>{matchSummaryText(m)}</div>")
w("              <div className='w-36 flex-shrink-0'>")
w("                {m && m.topScore > 0 ? (")
w("                  <span className={'inline-flex items-center px-2 py-0.5 rounded-full text-[10px] font-semibold border ' + scoreRangeColor(m.topScore)}>")
w("                    {scoreRange(m.topScore)}")
w("                  </span>")
w("                ) : (")
w("                  <span className='inline-flex items-center px-2 py-0.5 rounded-full text-[10px] font-semibold border bg-gray-50 text-gray-400 border-gray-200'>aring Match 분석중</span>")
w("                )}")
w("              </div>")
w("              <div className='w-10 flex-shrink-0 text-right text-[11px] text-aring-ink-400'>{item.view_count ?? 0}</div>")
w("            </div>")
w("          );")
w("        })}")
w("      </div>")
w("")
w("      {/* 모바일 카드 리스트 - comments 페이지와 동일한 구조 */}")
w("      <div className='lg:hidden mt-2 mx-4 rounded-2xl bg-white shadow-card border border-aring-ink-100 overflow-hidden divide-y divide-aring-ink-100'>")
w("        {listings.map((item, idx) => {")
w("          const m = matchMap[item.id] ?? null;")
w("          return (")
w("            <div key={item.id} onClick={() => router.push('/match/' + item.id)}")
w("              className='flex gap-3 px-4 py-3 hover:bg-aring-ink-50 transition-colors cursor-pointer active:bg-aring-ink-50'>")
w("              <div className='relative w-16 h-16 rounded-xl overflow-hidden bg-aring-ink-100 flex-shrink-0'>")
w("                {item.photo_url && <Image src={item.photo_url} alt={item.brand ?? ''} fill className='object-cover' />}")
w("              </div>")
w("              <div className='flex-1 min-w-0'>")
w("                <div className='flex items-center gap-1.5 mb-0.5'>")
w("                  <span className='text-[10px] font-bold text-aring-ink-400'>No.{listings.length - idx}</span>")
w("                  <span className={'inline-flex items-center px-1.5 py-0.5 rounded-full text-[10px] font-semibold border ' + statusColor(item.status)}>")
w("                    {statusLabel(item.status)}")
w("                  </span>")
w("                </div>")
w("                <p className='text-sm font-semibold text-aring-ink-900 truncate'>{item.brand ?? '브랜드 미상'}</p>")
w("                <p className='text-xs text-aring-ink-500 mt-0.5 leading-snug'>{matchSummaryText(m)}</p>")
w("                <div className='flex items-center gap-2 mt-1'>")
w("                  {m && m.topScore > 0 ? (")
w("                    <span className={'inline-flex items-center px-1.5 py-0.5 rounded-full text-[10px] font-semibold border ' + scoreRangeColor(m.topScore)}>")
w("                      {scoreRange(m.topScore)}")
w("                    </span>")
w("                  ) : (")
w("                    <span className='inline-flex items-center px-1.5 py-0.5 rounded-full text-[10px] font-semibold border bg-gray-50 text-gray-400 border-gray-200'>aring Match 분석중</span>")
w("                  )}")
w("                  <span className='text-[10px] text-aring-ink-400'>조회 {item.view_count ?? 0}</span>")
w("                </div>")
w("              </div>")
w("              <div className='flex-shrink-0 self-center text-aring-ink-300'>")
w("                <svg width='14' height='14' viewBox='0 0 24 24' fill='none' stroke='currentColor' strokeWidth='2'><polyline points='9 18 15 12 9 6'/></svg>")
w("              </div>")
w("            </div>")
w("          );")
w("        })}")
w("      </div>")
w("    </div>")
w("  );")
w("}")

path = os.path.join(base, "app", "my", "match", "page.tsx")
